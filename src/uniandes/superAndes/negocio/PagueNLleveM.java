package uniandes.superAndes.negocio;

public class PagueNLleveM extends Promocion {

	private int m;
	private int n;
	public PagueNLleveM(int m, int n) {
		super();
		this.m = m;
		this.n = n;
	}
	public int getM() {
		return m;
	}
	public void setM(int m) {
		this.m = m;
	}
	public int getN() {
		return n;
	}
	public void setN(int n) {
		this.n = n;
	}
	
	
}
